public class Main {
    public static void main(String[] args) {
        //uses of loop is counter

     //increment  Pre- & Post-
        int n = 10;
        System.out.println(n);
        System.out.println(n++);
        System.out.println(++n);
        System.out.println(n);


     // for loop get even number
        for (int i =1; i <= 100; i++){
            //even
            if( i % 2 ==0){
                System.out.println("even number is " + i);
            }

        }


    }
}
